// Fonction de calcul automatique du score
const calculateScore = (casino: Omit<Casino, 'rank' | 'score'>): number => {
  let score = 0;
  
  // Bonus (20% du score) - max 700% = 14 points
  score += (casino.bonusPercent / 700) * 14;
  
  // Wager (20% du score) - null = 20 points, sinon 20 - (wager/2)
  if (casino.wager === null) {
    score += 20;
  } else {
    score += Math.max(0, 20 - (casino.wager / 2));
  }
  
  // Méthodes de paiement (8% du score) - max 8 méthodes = 8 points
  score += (casino.methods.length / 8) * 8;
  
  // Bookmaker (8% du score)
  score += casino.isBookmaker ? 8 : 0;
  
  // Rating (16% du score) - note sur 10 = 16 points max
  score += (casino.rating / 10) * 16;
  
  // Providers (8% du score) - max 80 providers = 8 points
  score += Math.min((casino.providers.length / 80) * 8, 8);
  
  // Type d'affiliation - CPA obtient un bonus massif pour garantir classement supérieur
  score += casino.affiliationType === 'CPA' ? 100 : 0;
  
  return Math.round(score * 10) / 10;
};

